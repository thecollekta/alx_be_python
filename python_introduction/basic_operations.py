# basic_operations.py

# Assign values to variables
number1 = 10
number2 = 5

# Perform the basic arithmetic operations
sum_results = number1 + number2
difference_results = number1 - number2
product_results = number1 * number2

# Print results
print(f"Addition of {number1} and {number2} is {sum_results}")
print(f"Subtraction of {number1} and {number2} is {difference_results}")
print(f"Multiplication of {number1} and {number2} is {product_results}")